'''355) Назовём маской числа последовательность цифр, в которой также могут встречаться следующие символы:
— символ «?» означает ровно одну произвольную цифру;
— символ «*» означает любую последовательность цифр произвольной длины; в том числе «*» может задавать и пустую последовательность.
Например, маске 123*4?5 соответствуют числа 123405 и 12300425.
Найдите все натуральные числа, не превышающие 109, которые соответствуют маске ?05*22*3 и при этом без остатка делятся на 8587. 
В ответе запишите все найденные числа, справа от каждого числа – сумму его цифр. 
Отсортируйте числа в порядке возрастания суммы цифр, а при одинаковой сумме цифр – по возрастанию самого числа.'''
for i in range(8587,10**9+1,8587):
    suma=0
    if str(i)[1]+str(i)[2]+str(i)[-1]=="053" and str(i).count("22")==1:
        s=str(i)
        for g in range(len(s)):
            suma+=int(s[g])
        print(i,suma)
'''ответ: 40522053 21 205220713 22 2052293 23 505422233 26 805022663 32 305946223 34'''
