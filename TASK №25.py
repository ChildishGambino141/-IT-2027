'''214) Назовём маской числа последовательность цифр, в которой также могут встречаться следующие символы:
- символ «?» означает ровно одну произвольную цифру;
- символ «*» означает любую последовательность цифр произвольной длины; в том числе «*» может задавать и пустую последовательность.
Например, маске 123*4?5 соответствуют числа 123405 и 12300425. 
Найдите все натуральные числа, делящиеся нацело на 1011012, двоичный код которых соответствует маске 1?1?1?1?1??1. В ответе запишите найденные числа в десятичной системе счисления в порядке убывания, а справа от каждого числа – соответствующее частное от деления на 1011012.'''
for i in range(0,10**8+1,45):
    two=bin(i)[2:]
    if str(two)=="101010111001" or str(two)=="111111111111":
        print(i,i//45)
'''овтет: 2745 61 4095 91'''
